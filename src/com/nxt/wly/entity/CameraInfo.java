package com.nxt.wly.entity;
import java.io.Serializable;

public class CameraInfo implements Serializable{
private String dev_nickname;
private String dev_uid;
private String dev_name;
private String dev_pwd;
public String getDev_nickname() {
	return dev_nickname;
}
public void setDev_nickname(String dev_nickname) {
	this.dev_nickname = dev_nickname;
}
public String getDev_uid() {
	return dev_uid;
}
public void setDev_uid(String dev_uid) {
	this.dev_uid = dev_uid;
}
public String getDev_name() {
	return dev_name;
}
public void setDev_name(String dev_name) {
	this.dev_name = dev_name;
}
public String getDev_pwd() {
	return dev_pwd;
}
public void setDev_pwd(String dev_pwd) {
	this.dev_pwd = dev_pwd;
}
@Override
public String toString() {
	return "CameraInfo [dev_nickname=" + dev_nickname + ", dev_uid=" + dev_uid
			+ ", dev_name=" + dev_name + ", dev_pwd=" + dev_pwd + "]";
}

}
